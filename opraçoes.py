n1 = input("Digite um numero;")
n2 = input("Digite outro numero;")

# para realizar as opraçoes aritimeticas, ser
# necessario converter as variaveis n1 e n2 e em
# tipos numerios, quepodem ser; int ou float

n1 = int(n1)
n2 = int(n2)


soma = n1 + n2
subitrair = n1 - n2
multiplicar = n1 * n2
dividir = n1 / n2

print(f"A soma entre os numeros {n1} e {n2} e {soma}")
print(f"A subtraçao entre os numeros {n1} e {n2} e {subitrair}")
print(f"A multiplicaçao entre os numeros {n1} e {n2} e {multiplicar}")
print(f"A divisao entre os numeros {n1} e {n2} e {dividir}")
